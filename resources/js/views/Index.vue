            this.getMyBirds.forEach(elem => {
                if (+elem.fertility > maxFertility) {
                    maxFertility = +elem.fertility;
                    birdName     = elem.name
                }
            });

            return {
                fertility: Math.round(maxFertility),
                name     : birdName
            };
        },
        /**
         * get the most pooping bird
